#include "ft_header.h"

void	*ft_memcpy(void *str1, const void * str2, size_t n)
{
	char	*s1;
	char	*s2;
	size_t	i;

	s1 = str1;
	s2 = str2;
	while (i < n)
	{
		s1 = s2;
		s1++;
		s2++;
	}
	return (s1);
}
